<h1>Translations</h1>

<p>
As of version 1.9.4, Buddi includes a language editor which simplifies most aspects of doing the translation.  You can access it via Preferences -> Advanced, and click on the Edit Languages button.
</p>

<p>
This editor takes care of most of the technical issues of translation.  You can enter special characters directly into the edit pane; you can easily see which terms have not been translated, are the same as the base language, etc.  (Click on Help on the bottom of the window to see a list of color codes and their meanings).
</p>

<p>
The edited language files are automatically saved to the Languages folder in the working directory (hopefully the directory where Buddi is located, assuming proper shortcuts are used).
</p>

<p>
Please give me feedback on the language editor, and let me know if you find it useful, and what changes you would suggest.
</p>

<p>
All translations have been done by Buddi users - if you send me a copy of your translation, I will include your language in the next release, so that everyone can enjoy it.
</p>

